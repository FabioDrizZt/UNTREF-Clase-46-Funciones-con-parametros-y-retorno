# Ejercicio 1: Validación de Texto
# 
# Escribe una función que valide las siguientes condiciones para un texto dado:
# 1. El texto debe comenzar y terminar con una letra mayúscula.
# 2. Solo puede contener letras y espacios.
# 3. Su longitud debe estar entre 10 y 50 caracteres.
# Si alguna condición no se cumple, la función debe lanzar una excepción personalizada llamada `TextoInvalidoError`.